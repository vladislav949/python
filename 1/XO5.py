#***********************   Наполнение списка пользователем   *************************************

n = int(input("Введите длину списка: "))

user_list = [] # изначально пустой список
print("Пустой список", user_list)
i = 0
while i < n: # Цикл будет длиться до тех пор, пока i будет меньше числа которое ввел пользователь
    string = "Enter element" + str(i + 1) + ": " # str(i + 1) чтобы список начинался не с 0
    user_list.append(input(string))
    i += 1 # внутри цикла i каждый раз будет увеличиваться на 1

print("Список создан", user_list)




