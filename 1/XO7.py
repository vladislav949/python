print("Кортежи (tuple) ------------------------------------------")
#  Грубо говоря, кортеж - это список, который нельзя изменить. То есть, в кортеже есть только права на чтение.
#  Это может быть защитой от случайных изменений. Также Кортежи весят меньше списков.
cortege = (4, 5, 2, "text", True)   # Для создания списка мы используем []-скобки, а для кортежа ()-скобки обычные.
print(cortege[1])
